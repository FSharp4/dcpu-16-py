public class incrementInvestigate {
    public static void main(String[] args) {
        int a = 0;
        System.out.println("a++ = " + (a++) + " at time of execution");
        a = 0;
        System.out.println("++a = " + (++a) + " at time of execution");
        a = 0;
        System.out.println("a-- = " + (a--) + " at time of execution");
        a = 0;
        System.out.println("--a = " + (--a) + " at time of execution");
    }
}
